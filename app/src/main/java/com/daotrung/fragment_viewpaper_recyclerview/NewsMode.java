package com.daotrung.fragment_viewpaper_recyclerview;

public class NewsMode {
    public int img ;
    public String name ;

    public NewsMode(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
